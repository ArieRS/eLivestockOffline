package com.ui.model.laporan;

public class Search_KejadianKematianModel {
	String TanggalKematian;
	String SebabKematian;
	String LokasiKematian;
	String IdPetugas;
	
	
	public String getTanggalKematian() {
		return TanggalKematian;
	}
	public void setTanggalKematian(String tanggalKematian) {
		TanggalKematian = tanggalKematian;
	}
	public String getSebabKematian() {
		return SebabKematian;
	}
	public void setSebabKematian(String sebabKematian) {
		SebabKematian = sebabKematian;
	}
	public String getLokasiKematian() {
		return LokasiKematian;
	}
	public void setLokasiKematian(String lokasiKematian) {
		LokasiKematian = lokasiKematian;
	}
	public String getIdPetugas() {
		return IdPetugas;
	}
	public void setIdPetugas(String idPetugas) {
		IdPetugas = idPetugas;
	}
}
