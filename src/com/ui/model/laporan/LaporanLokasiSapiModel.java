package com.ui.model.laporan;

public class LaporanLokasiSapiModel {
	
	String idLokasi;
	String namaKontak;
	String kabupatenKota;
	String alamat;
	String noTelepon;
	String Type;
	String Petugas;
	
	public String getIdLokasi() {
		return idLokasi;
	}
	public void setIdLokasi(String idLokasi) {
		this.idLokasi = idLokasi;
	}
	public String getNamaKontak() {
		return namaKontak;
	}
	public void setNamaKontak(String namaKontak) {
		this.namaKontak = namaKontak;
	}
	public String getKabupatenKota() {
		return kabupatenKota;
	}
	public void setKabupatenKota(String kabupatenKota) {
		this.kabupatenKota = kabupatenKota;
	}
	public String getAlamat() {
		return alamat;
	}
	public void setAlamat(String alamat) {
		this.alamat = alamat;
	}
	public String getNoTelepon() {
		return noTelepon;
	}
	public void setNoTelepon(String noTelepon) {
		this.noTelepon = noTelepon;
	}
	public String getType() {
		return Type;
	}
	public void setType(String type) {
		Type = type;
	}
	public String getPetugas() {
		return Petugas;
	}
	public void setPetugas(String petugas) {
		Petugas = petugas;
	}
}
