package com.ui.model.laporan;

public class LaporanAIModel {
	String kodeStraw;
	String nit;
	String idLokasi;
	String namaKontak;
	String lokasi;
	
	public String getKodeStraw() {
		return kodeStraw;
	}
	public void setKodeStraw(String kodeStraw) {
		this.kodeStraw = kodeStraw;
	}
	public String getNit() {
		return nit;
	}
	public void setNit(String nit) {
		this.nit = nit;
	}
	public String getIdLokasi() {
		return idLokasi;
	}
	public void setIdLokasi(String idLokasi) {
		this.idLokasi = idLokasi;
	}
	public String getNamaKontak() {
		return namaKontak;
	}
	public void setNamaKontak(String namaKontak) {
		this.namaKontak = namaKontak;
	}
	public String getLokasi() {
		return lokasi;
	}
	public void setLokasi(String lokasi) {
		this.lokasi = lokasi;
	}
	
}
